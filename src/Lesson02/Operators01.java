package Lesson02;

public class Operators01 {

    public static void main(String[] args) {

        int i = 3;
        i++;

        System.out.println(i);

        ++i;

        System.out.println(i);
        System.out.println(++i);
        System.out.println(i++);
        System.out.println(i);

        i *= 2;
        i -= 10;

        System.out.println(i);


    }
}
