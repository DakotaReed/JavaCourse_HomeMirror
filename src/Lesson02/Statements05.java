package Lesson02;

public class Statements05 {

    public static void main(String[] args) {

        int i=8;

        if (i>=0 && i<=6)
            System.out.println("Go to Kindergarten");
        else if (i>=7 && i<=18)
            System.out.println("Go to School");
        else if (i>=19 && i<=67)
            System.out.println("Go to Work");
        else if (i>=68 && i<=120)
            System.out.println("Collecting Pension");

    }
}
